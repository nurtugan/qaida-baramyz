// Confirm Delete Button
function Delete(url) {
    swal({
        title: "Are you sure?",
        text: "Once deleted, you will not be able to recover",
        icon: "warning",
        buttons: true,
        dangerMode: true
    }).then((willDelete) => {
        if (willDelete) {
            $.ajax({
                type: "DELETE",
                url: url,
                success: function (data) {
                    if (data.success) {
                        toastr.success(data.message);
                        setTimeout("location.reload(true);", 500);
                    }
                    else {
                        toastr.error(data.message);
                        //console.log(url);
                    }
                }
            });
        }
    });
}